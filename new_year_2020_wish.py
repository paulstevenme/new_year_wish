BG_COLOR = "#0080ff"

myturtle = Turtle()
# set turtle speed
myturtle.speed(9)
myturtle.pensize(20)

screen = myturtle.getscreen()
# set background color
screen.bgcolor(BG_COLOR)
# set tile of screen
screen.title("Happy New Year")
# maximize the screen
screen.setup(width=1.0, height=1.0)


# create tree trunk
h(myturtle, "aqua", -400, 300, 30, 60)
