# create a star a top of tree
# now add few stars in sky
myturtle.speed(10)
number_of_stars = randint(0,10)
# print(number_of_stars)
for _ in range(0,number_of_stars):
    x_star = randint(-(screen.window_width()//2),screen.window_width()//2)
    y_star = randint(60, screen.window_height()//2)
    size = randint(5,20)
    myturtle.pensize(2)
    myturtle.penup()
    myturtle.color('gold')
    myturtle.goto(x_star, y_star)
    myturtle.begin_fill()
    myturtle.pendown()
    for i in range(5):
        myturtle.forward(size)
        myturtle.right(144)
    myturtle.end_fill()

screen.mainloop()
